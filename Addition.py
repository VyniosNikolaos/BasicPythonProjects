# Basic addition
a = 5
b = 3
result = a + b
print(f"{a} + {b} = {result}")

# Adding multiple numbers
c = 2
sum_result = a + b + c
print(f"{a} + {b} + {c} = {sum_result}")

# Adding integers and floats
d = 1.5
float_result = a + d
print(f"{a} + {d} = {float_result}")

# Using the += operator
x = 10
x += 5  # Equivalent to x = x + 5
print(f"After x += 5, x = {x}")