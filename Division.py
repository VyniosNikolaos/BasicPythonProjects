# Basic division (always returns a float)
a = 10
b = 3
result = a / b
print(f"{a} / {b} = {result}")

# Integer division (floor division)
int_result = a // b
print(f"{a} // {b} = {int_result}")

# Dividing with floats
c = 2.5
float_result = a / c
print(f"{a} / {c} = {float_result}")

# Using the /= operator
x = 20
x /= 4  # Equivalent to x = x / 4
print(f"After x /= 4, x = {x}")