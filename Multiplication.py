# Basic multiplication
a = 4
b = 3
result = a * b
print(f"{a} * {b} = {result}")

# Multiplying multiple numbers
c = 2
product_result = a * b * c
print(f"{a} * {b} * {c} = {product_result}")

# Multiplying integers and floats
d = 1.5
float_result = a * d
print(f"{a} * {d} = {float_result}")

# Using the *= operator
x = 5
x *= 3  # Equivalent to x = x * 3
print(f"After x *= 3, x = {x}")