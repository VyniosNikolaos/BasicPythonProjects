# Basic subtraction
a = 10
b = 3
result = a - b
print(f"{a} - {b} = {result}")

# Subtracting multiple numbers
c = 2
diff_result = a - b - c
print(f"{a} - {b} - {c} = {diff_result}")

# Subtracting integers and floats
d = 1.5
float_result = a - d
print(f"{a} - {d} = {float_result}")

# Using the -= operator
x = 20
x -= 5  # Equivalent to x = x - 5
print(f"After x -= 5, x = {x}")