# String to Integer
str_num = "42"
int_num = int(str_num)
print(f"'{str_num}' converted to int: {int_num}")

# Integer to String
num = 100
str_num = str(num)
print(f"{num} converted to string: '{str_num}'")

# Float to Integer (truncates decimal part)
float_num = 3.14
int_from_float = int(float_num)
print(f"{float_num} converted to int: {int_from_float}")

# Integer to Float
int_num = 5
float_from_int = float(int_num)
print(f"{int_num} converted to float: {float_from_int}")

# String to Float
str_float = "3.14"
float_num = float(str_float)
print(f"'{str_float}' converted to float: {float_num}")

# Float to String
float_num = 2.71828
str_float = str(float_num)
print(f"{float_num} converted to string: '{str_float}'")


