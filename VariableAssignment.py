# Assigning a value to a variable
x = 5
print(f"x = {x}")

# Assigning multiple variables at once
a, b, c = 1, 2, 3
print(f"a = {a}, b = {b}, c = {c}")

# Swapping variables
x, y = 10, 20
print(f"Before swap: x = {x}, y = {y}")
x, y = y, x
print(f"After swap: x = {x}, y = {y}")

# Assigning the same value to multiple variables
m = n = p = 0
print(f"m = {m}, n = {n}, p = {p}")